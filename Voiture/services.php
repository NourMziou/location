   location :</strong></span></h3>
   <p>3-1 - Etat du véhicule Un état descriptif du véhicule est joint à votre contrat. Vous vous engagez à y consigner par écrit, avant
   votre départ de la station, toute défectuosité apparente qui n'y figurerait pas.
      A défaut, nous sommes réputés avoir délivré un véhicule conforme à l'état descriptif. Nous ne pourrons malheureusement pas tenir
	  compte de réclamations concernant des dégâts apparents qui n'auront pas été signalés au moment du départ.
       Vous devez rendre le véhicule dans l'état où vous l'avez reçu. Tous frais de remise en état, consécutifs à une faute du locataire,
	   viendront en surcharge du coût de la location, sous réserve des stipulations de la section "garanties contractuelles optionnelles".</p>
   <p>&nbsp;3-2 - Usage du véhicule Vous ne devez jamais faire circuler le véhicule ailleurs qu'en Tunisie. Conformément au principe de
   personnalité des peines, vous êtes responsable des infractions commises pendant la durée de la location.
      Ainsi, vous êtes informé que vos coordonnées pourront être communiquées aux autorités de police, de la garde nationale ou judiciaires
	  qui en feraient la demande. Vous vous engagez à utiliser le véhicule en "bon père de famille" et notamment,
       sans être sous influence éthylique ou narcotique ou de toute substance susceptible d'affecter la conduite conformément aux
	   dispositions du code de la Route et l'utiliser conformément à sa destination.
      </p>
   <p>ATTENTION :En cas de mauvaise appréciation du gabarit du véhicule. Les chocs hauts de caisse et bas de caisse ne sont pas couverts
   par la garantie dommages sauf à prouver le cas de force majeure.
     Vous ne devez pas vous servir du véhicule loué notamment : pour être reloué ; pour le transport de personnes à titre onéreux ; pour le
	 transport d'un nombre de personnes supérieur à celui mentionné sur la carte grise du véhicule ;
