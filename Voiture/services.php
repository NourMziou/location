       sur des routes non carrossables ou dont la surface ou l'état d'entretien présente des risques pour les pneus ou les organes sous le
	   véhicule ; * pour commettre une infraction intentionnelle.
       Les marchandises et bagages transportés dans le véhicule, y compris leur emballage ou leur arrimage, ne doivent ni détériorer le
	   véhicule, ni faire courir de risques anormaux à ses occupants.
        Quand vous stationnez le véhicule, même pour un arrêt de courte durée, vous vous engagez à fermer le véhicule à clef.</p>
   <h3><span style="text-decoration: underline; color: #808080;"><strong>4 - Location de voiture en Tunisie : Durée de la location :
   </strong></span></h3>
   <p>4-1 - Notion de calcul Le locataire s'engage à restituer le véhicule au loueur au jour et à l'heure prévue au contrat de location
   sous peine de s'exposer à des poursuites judiciaires civiles et pénales ainsi qu'à la facturation de frais de retard.
      Une franchise de 179 minutes est accordée. Un maximum de deux (2) heures supplémentaires pourra être facturé. Au-delà de ces cinq
	  heures, une journée entière sera facturée.</p>
   <p>ATTENTION : La durée d'un contrat de location est de 1 moi maximum, sans aucune prolongation possible.</p>
   <p>ATTENTION : Il n'y a pas lieu à remboursement pour toute location plus courte que la durée prévue dans la réservation.</p>
   <p>4-2 - Prolongation de la location Le locataire doit demander au loueur, au moins 48 heures à l'avance, la prolongation de la location
   en l'accompagnant de la provision correspondante,
     sous peine de s'exposer à des poursuites judiciaires pour détournement de véhicule et abus de confiance. Cependant, le loueur se
	 réserve le droit de refuser la prolongation de la location,
     sans indemnité pour le locataire, avec obligation pour celui-ci de restituer immédiatement le véhicule.</p>
   <p>4-3 - Fin de la location La location se termine par la restitution du véhicule, de ses clefs et de ses papiers au comptoir du loueur,
   à un agent de l'agence .
      Avec badge. En aucun cas vous ne restituerez les clefs à des personnes présentes sur les parkings et prétendant être agent  .
