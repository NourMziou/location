   générales :</strong></span></h3>
   <p>Les différentes notions auxquelles font référence les présentes conditions devront être interprétées au regard de la Recommandation
   de la Commission des Clauses Abusives et du droit commun applicable aux contrats de louage régis,
      en droit Tunisien, par les articles 726 et 727 du Code des obligations et des contrats. Le contrat de location est conclu intuitif
	  personnes et ne saurait faire l'objet d'une cession.
       "Vous", "le locataire" désigne les conducteurs et les payeurs mentionnés sur le contrat de location et signataires de celui-ci qui
	   ont la qualité de locataire. "Nous", "le loueur" désigne l'application ,
        dont la raison sociale figure sur le Contrat de Location. "véhicule" désigne une Voiture
		que nous vous louons pour la durée convenue du Contrat de Location.
        "Dommages" : est considéré comme dommage, tout dégât survenu au véhicule y compris le bris de glace, ce dernier incluant les
		optiques, les rétroviseurs et les phares.
        "Vol" : est assimilé au vol du véhicule : le vandalisme, le vol d'accessoires et la tentative de vol. "La Tunisie" désigne le
		pays dans lequel le véhicule loué peut circuler.
        La circulation en dehors de la Tunisie est interdite, sauf autorisation expresse de l'agence.</p>
   <h3><span style="text-decoration: underline; color: #808080;"><strong>&nbsp;2 - Location de voiture en Tunisie :&nbsp;Conditions à
   remplir pour louer un véhicule :</strong></span></h3>
   <p>Vous devez nous fournir, avec justificatifs, tous les renseignements indispensables à l'établissement de votre contrat de location
   et notamment : votre identité (passport),
     votre adresse (domicile et e-mail), vos coordonnées téléphoniques fixes, le numéro et la date de délivrance et d'expiration de votre
	 permis de conduire ainsi que son lieu d'émission.
     Le moyen de paiement de votre location est fixé selon le choix du locataire que ce soit en ligne ou en agence. A défaut de communication de ces données la demande de réservation ne pourra être validée.
     Préalablement à la remise du véhicule sera exigée une seconde forme d'identification par la production d'un petit compte-rendu
     décrivant l'etat de voiture.
	</p>
   <p>ATTENTION :
       Les Cartes de crédit Electron, Maestro et Aurore ne sont pas acceptées Tout conducteur doit être titulaire depuis au moins 24 mois
	   d'un permis de conduire, en cours de validité,
       et correspondant à la catégorie de véhicule loué. Il doit être également âgé de plus de 22 ans (l'âge requis peut être plus élevé
	   pour certaines catégories de véhicules). </p>
