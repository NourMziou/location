       à la facturation de frais de nettoyage forfaitaire de 40 DTN. La propreté de la voiture sera déterminée pendant l'inspection du
	   véhicule qui sera conduite en votre présence par un agent Avantgarde Auto-Passion .
       Le véhicule vous est fourni sans le plein de carburant mais avec un quart (1/4) de carburant et vous devez le restituer dans le même
	   état. Si le véhicule restitué est sur la réserve (voyant de la jauge allumée),
       des frais forfaitaires pour " service essence " de 20 DTN vous seront facturés. Nous vous invitons à assister à l'inspection du
	   véhicule lors de sa restitution et de contresigner avec un de nos agents le rapport d'inspection avant de partir, de manière à
	   constater conjointement tout dégât et d'éviter tout désagrément aux deux parties. Si toutefois vous décidez de quitter le parking
	   et de pas attendre l'inspection, il vous sera demandé de signer le formulaire d'inspection par avance, matérialisant votre accord
	   pour que nous procédions à l'inspection du véhicule en votre absence, et en conséquence, si un dégât est constaté sur votre véhicule,
	   si le véhicule n'est pas propre, si la jauge est sur la réserve, si le kilométrage alloué est dépassé, nous procèderons à la
	   facturation des suppléments correspondants conformément aux dispositions de l'article5. Dans l'hypothèse où le véhicule serait
	   restitué sans ses clés un montant forfaitaire de 150 DTN vous sera facturé, ainsi que, s'il y a lieu, les frais de rapatriement
	   du véhicule conformément aux dispositions de l'article5. Le loueur ne peut en aucune façon être tenu responsable des biens qui
	   auraient été oubliés dans les véhicules à l'issue de la location. ATTENTION : Seule la prise de possession du véhicule, des documents
	   et des clefs par l'agent, aux heures d'ouverture de l'agence concernée, permettent de mettre fin au contrat de location. Rappel :
	   votre responsabilité est engagée jusqu'à la fin du contrat de location. Exceptions : en cas de confiscation ou de mise sous scellés
	   du véhicule, le contrat de location pourra être résilié de plein droit dès que le loueur en sera informé par les autorités judiciaires
	   ou par le locataire. Toute utilisation du véhicule qui porterait préjudice au loueur autoriserait celui-ci à résilier de plein droit
	   le contrat. En cas de vol, le contrat de location est arrêté dès transmission au loueur du dépôt de plainte effectué par le locataire
	   auprès des autorités compétentes. En cas d'accident, le contrat de location est arrêté dès transmission au loueur du constat amiable
	   dûment rempli par le locataire et le tiers éventuel.</p>
